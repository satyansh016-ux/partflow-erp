# --- Ensure a backup encryption key exists BEFORE importing the app --------
# config.py calls load_dotenv() the moment it's imported (which happens as
# soon as `from app import create_app` runs below), so the key must already
# be on disk in .env before that import, not just in os.environ.
_basedir = os.path.dirname(os.path.abspath(__file__))
_env_path = os.path.join(_basedir, ".env")
_env_text = ""
if os.path.exists(_env_path):
    with open(_env_path, "r") as _f:
        _env_text = _f.read()

_has_key = any(
    line.startswith("BACKUP_ENCRYPTION_KEY=") and line.strip() != "BACKUP_ENCRYPTION_KEY="
    for line in _env_text.splitlines()
)
if not _has_key:
    from cryptography.fernet import Fernet
    _new_key = Fernet.generate_key().decode()
    with open(_env_path, "a") as _f:
        _f.write(f"\n# Auto-generated by seed.py — required to decrypt backups. Keep this safe!\n")
        _f.write(f"BACKUP_ENCRYPTION_KEY={_new_key}\n")
    print(f"[OK] Generated BACKUP_ENCRYPTION_KEY and saved it to .env")

if "BACKUP_TRIGGER_SECRET=" not in _env_text:
    import secrets as _secrets
    _new_secret = _secrets.token_hex(24)
    with open(_env_path, "a") as _f:
        _f.write(f"BACKUP_TRIGGER_SECRET={_new_secret}\n")
    print(f"[OK] Generated BACKUP_TRIGGER_SECRET and saved it to .env (for external cron use)")

_has_vapid = any(
    line.startswith("VAPID_PRIVATE_KEY=") and line.strip() != "VAPID_PRIVATE_KEY="
    for line in _env_text.splitlines()
)
if not _has_vapid:
    import base64
    from cryptography.hazmat.primitives.asymmetric import ec
    from cryptography.hazmat.primitives import serialization

    _vapid_key = ec.generate_private_key(ec.SECP256R1())
    # py_vapid (used inside pywebpush) expects base64url-encoded DER - NOT PEM.
    _der_bytes = _vapid_key.private_bytes(
        encoding=serialization.Encoding.DER,
        format=serialization.PrivateFormat.PKCS8,
        encryption_algorithm=serialization.NoEncryption(),
    )
    _private_str = base64.urlsafe_b64encode(_der_bytes).rstrip(b"=").decode()

    _public_bytes = _vapid_key.public_key().public_bytes(
        encoding=serialization.Encoding.X962,
        format=serialization.PublicFormat.UncompressedPoint,
    )
    _public_b64 = base64.urlsafe_b64encode(_public_bytes).rstrip(b"=").decode()

    with open(_env_path, "a") as _f:
        _f.write(f"\n# Auto-generated by seed.py — Web Push (VAPID) keys for browser push notifications\n")
        _f.write(f"VAPID_PRIVATE_KEY={_private_str}\n")
        _f.write(f"VAPID_PUBLIC_KEY={_public_b64}\n")
    print(f"[OK] Generated VAPID push-notification keys and saved them to .env")

# --- IMPORTANT: ephemeral hosting filesystems (Render, Railway, etc.) ------
# The generated values above are written to a LOCAL .env file. On a platform
# whose filesystem resets on every deploy, this file (and everything just
# generated into it) disappears on the next deploy and gets silently
# regenerated with NEW random values — which would make old backups
# undecryptable and break any saved push subscriptions.
#
# If you're running this on Render/Railway/etc., after this first run, open
# the container's live .env (Render: Shell tab → `cat .env`) and copy
# BACKUP_ENCRYPTION_KEY, BACKUP_TRIGGER_SECRET, VAPID_PRIVATE_KEY, and
# VAPID_PUBLIC_KEY into your platform's permanent Environment Variables
# dashboard, so they stay fixed across every future deploy.

