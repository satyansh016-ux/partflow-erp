def run():
    with app.app_context():
        db.create_all()

        # --- Super Admin -------------------------------------------------------
        admin_email = app.config["SUPERADMIN_EMAIL"]
        if not User.query.filter_by(email=admin_email).first():
            admin = User(name="Platform Admin", email=admin_email, role=Role.SUPER_ADMIN.value)
            admin.set_password(app.config["SUPERADMIN_PASSWORD"])
            db.session.add(admin)
            db.session.commit()
            print(f"[OK] Super admin created: {admin_email}")
        else:
            print(f"[SKIP] Super admin already exists: {admin_email}")

        # --- Help Center content -------------------------------------------------
        seed_help_articles()

        # --- Demo shop -----------------------------------------------------------
        if Shop.query.filter_by(name="Sharma Auto Spares").first():
            print("[SKIP] Demo shop already exists. Nothing more to seed.")
            return

        shop = Shop(
            name="Sharma Auto Spares", gst_number="27ABCDE1234F1Z5",
            phone="9876543210", address="Shop No. 4, Wardha Road, Nagpur",
            status=ShopStatus.ACTIVE.value,
        )
        db.session.add(shop)
        db.session.flush()

        sub = Subscription(
            shop_id=shop.id, plan_name="Standard", amount=999,
            start_date=date.today(), end_date=date.today() + timedelta(days=365),
            status="active",
        )
        db.session.add(sub)

        owner = User(shop_id=shop.id, name="Ramesh Sharma", email="owner@sharmaauto.com",
                     role=Role.OWNER.value, phone="9876543210")
        owner.set_password("Owner@123")
        db.session.add(owner)

        employee = User(shop_id=shop.id, name="Vikram", email="employee@sharmaauto.com",
                         role=Role.EMPLOYEE.value, phone="9123456780")
        employee.set_password("Employee@123")
        db.session.add(employee)
        db.session.flush()

        supplier = Supplier(shop_id=shop.id, name="Bajaj Auto Distributors",
                             phone="9988776655", gst_number="27SUPPL1234F1Z9")
        db.session.add(supplier)
        db.session.flush()

        main_godown = get_default_godown(shop.id)
        main_godown.name = "Main Godown (Wardha Road)"
        branch_godown = Godown(shop_id=shop.id, name="Ring Road Branch Godown",
                                address="Ring Road, Nagpur")
        db.session.add(branch_godown)
        db.session.flush()

        demo_parts = [
            dict(name="Front Brake Pad Set", oem_number="BP-1023", vehicle_type="bike",
                 vehicle_model="Pulsar 150", brand="Bajaj", category="Brakes",
                 purchase_price=250, selling_price=400, gst_percent=18, hsn_code="8714",
                 minimum_stock=10, rack_location="A1", opening=25),
            dict(name="Engine Oil Filter", oem_number="OF-2201", vehicle_type="car",
                 vehicle_model="Swift", brand="Maruti", category="Engine",
                 purchase_price=120, selling_price=220, gst_percent=18, hsn_code="8421",
                 minimum_stock=15, rack_location="B2", opening=8),
            dict(name="Heavy Duty Clutch Plate", oem_number="CP-3390", vehicle_type="truck",
                 vehicle_model="Tata 1613", brand="Tata OEM", category="Clutch",
                 purchase_price=1800, selling_price=2600, gst_percent=28, hsn_code="8708",
                 minimum_stock=5, rack_location="C1", opening=3),
            dict(name="Hydraulic Pump Seal Kit", oem_number="HP-4410", vehicle_type="jcb",
                 vehicle_model="3DX", brand="JCB Genuine", category="Hydraulics",
                 purchase_price=950, selling_price=1400, gst_percent=18, hsn_code="8413",
                 minimum_stock=6, rack_location="D1", opening=2),
            dict(name="Tractor Front Axle Bush", oem_number="AB-5120", vehicle_type="tractor",
                 vehicle_model="Mahindra 575", brand="Mahindra", category="Suspension",
                 purchase_price=340, selling_price=520, gst_percent=18, hsn_code="8708",
                 minimum_stock=8, rack_location="E1", opening=12),
        ]

        parts_by_name = {}
        for pd in demo_parts:
            opening = pd.pop("opening")
            part = Part(shop_id=shop.id, supplier_id=supplier.id,
                        purchase_price=Decimal(pd["purchase_price"]),
                        selling_price=Decimal(pd["selling_price"]),
                        gst_percent=Decimal(pd["gst_percent"]), **{k: v for k, v in pd.items()
                        if k not in ("purchase_price", "selling_price", "gst_percent")})
            db.session.add(part)
            db.session.flush()

            if part.name == "Front Brake Pad Set":
                # Split across two godowns to demonstrate the stock-transfer feature.
                main_qty = opening - (opening // 3)
                branch_qty = opening // 3
                apply_stock_change(part, main_qty, "adjustment", owner.id,
                                    reference_type="opening_stock", godown_id=main_godown.id)
                apply_stock_change(part, branch_qty, "adjustment", owner.id,
                                    reference_type="opening_stock", godown_id=branch_godown.id)
            else:
                apply_stock_change(part, opening, "adjustment", owner.id, reference_type="opening_stock")
            parts_by_name[part.name] = part

        labour = LabourCharge(shop_id=shop.id, name="Fitting Charge", price=Decimal("150"))
        db.session.add(labour)
        db.session.add(LabourCharge(shop_id=shop.id, name="Service Charge", price=Decimal("300")))

        # A sample customer + invoice so the dashboard isn't empty on first login
        customer = Customer(shop_id=shop.id, name="Anil Kumar", mobile="9090909090",
                             vehicle_number="MH31AB1234", vehicle_type="bike")
        db.session.add(customer)
        db.session.flush()

        part = parts_by_name["Front Brake Pad Set"]
        invoice = Invoice(
            shop_id=shop.id, invoice_number=generate_invoice_number(shop.id),
            customer_id=customer.id, vehicle_number=customer.vehicle_number,
            vehicle_type=customer.vehicle_type, created_by=owner.id,
            payment_method=PaymentMethod.CASH.value, payment_status=PaymentStatus.PAID.value,
        )
        db.session.add(invoice)
        db.session.flush()

        qty = 2
        line_total = qty * part.selling_price
        db.session.add(InvoiceItem(
            invoice_id=invoice.id, part_id=part.id, part_name_snapshot=part.name,
            quantity=qty, unit_price=part.selling_price, gst_percent=part.gst_percent,
            line_total=line_total,
        ))
        apply_stock_change(part, -qty, "sale", owner.id, reference_type="invoice", reference_id=invoice.id)

        gst_amt = line_total * part.gst_percent / (100 + part.gst_percent)
        invoice.subtotal = line_total
        invoice.gst_total = gst_amt
        invoice.grand_total = line_total
        invoice.amount_paid = line_total

        db.session.commit()

        print("[OK] Demo shop 'Sharma Auto Spares' created with sample data.")
        print("     Owner login:    owner@sharmaauto.com / Owner@123")
        print("     Employee login: employee@sharmaauto.com / Employee@123")


if __name__ == "__main__":
    run()

